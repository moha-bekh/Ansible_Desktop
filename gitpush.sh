#!/bin/bash

default="\033[0m"
purple="\033[35m"

echo -e ${purple}"\n########################################################"${default}
echo -e ${purple}"#----------------------- STATUS -----------------------#"${default}
echo -e ${purple}"########################################################\n"${default}
git status
echo -e ${purple}"\n########################################################"${default}
echo -e ${purple}"#------------------------- ADD ------------------------#"${default}
echo -e ${purple}"########################################################\n"${default}
git add .
git status
echo -e ${purple}"\n########################################################"${default}
echo -e ${purple}"#----------------------- COMMIT -----------------------#"${default}
echo -e ${purple}"########################################################\n"${default}
export date="$(date '+%Y-%m-%d')"

if [ $# -eq 0 ]; then
	git commit -m "Work In Progress $date"
else
	git commit -m "$1 $date"
fi
echo -e ${purple}"\n########################################################"${default}
echo -e ${purple}"#----------------------- STATUS -----------------------#"${default}
echo -e ${purple}"########################################################\n"${default}
git status
echo -e ${purple}"\n########################################################"${default}
echo -e ${purple}"#------------------------ PUSH ------------------------#"${default}
echo -e ${purple}"########################################################\n"${default}
git push
echo -e ${purple}"\n########################################################"${default}
echo -e ${purple}"#------------------------- END ------------------------#"${default}
echo -e ${purple}"########################################################\n"${default}
